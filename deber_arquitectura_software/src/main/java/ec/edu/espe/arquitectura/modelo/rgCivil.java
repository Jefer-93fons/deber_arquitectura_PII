public class rgCivil {
    String cedu;
    String apel;
    String nomb;
    String fecN;
    String codP;
    String gene;
    String estC;

    public String getCedu() {
        return cedu;
    }

    public void setCedu(String cedu) {
        this.cedu = cedu;
    }

    public String getApel() {
        return apel;
    }

    public void setApel(String apel) {
        this.apel = apel;
    }

    public String getNomb() {
        return nomb;
    }

    public void setNomb(String nomb) {
        this.nomb = nomb;
    }

    public String getFecN() {
        return fecN;
    }

    public void setFecN(String fecN) {
        this.fecN = fecN;
    }

    public String getCodP() {
        return codP;
    }

    public void setCodP(String codP) {
        this.codP = codP;
    }

    public String getGene() {
        return gene;
    }

    public void setGene(String gene) {
        this.gene = gene;
    }

    public String getEstC() {
        return estC;
    }

    public void setEstC(String estC) {
        this.estC = estC;
    }
    
}
